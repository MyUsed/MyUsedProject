package choice;

public class choiceDTO {
	private int c_no, price, mem_num;
	private String content, pro_pic, mem_name, reg;
	
	public int getC_no() {return c_no;}
	public void setC_no(int c_no) {this.c_no = c_no;}
	
	public int getPrice() {return price;}
	public void setPrice(int price) {this.price = price;}
	
	public int getMem_num() {return mem_num;}
	public void setMem_num(int mem_num) {this.mem_num = mem_num;}
	
	public String getContent() {return content;}
	public void setContent(String content) {this.content = content;}
	
	public String getPro_pic() {return pro_pic;}
	public void setPro_pic(String pro_pic) {this.pro_pic = pro_pic;}
	
	public String getMem_name() {return mem_name;}
	public void setMem_name(String mem_name) {this.mem_name = mem_name;}
	
	public String getReg() {return reg;}
	public void setReg(String reg) {this.reg = reg;}
}
